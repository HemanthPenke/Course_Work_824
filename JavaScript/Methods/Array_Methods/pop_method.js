// -------------------Pop Method-------------------------

// Pop Method is used to remove last elements of the array

arr = [24, 43, 54, 76, 45, 34, 87, 27]

console.log("Before Using Pop Method  : ", arr)

arr.pop();

console.log("After Using Pop Method  : ", arr)



/*

--------------------Output-------------------

Before Using Pop Method  :  [ 24, 43, 54, 76, 45, 34, 87, 27 ]
After Using Pop Method  :  [ 24, 43, 54, 76, 45, 34, 87 ]

*/