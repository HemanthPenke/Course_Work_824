//---------------------toUpperCase method----------------------

//return the all characters into the Upper case

//The toUpperCase() method does not change the original string.

var sample ="Hii I am Penke Hemanth Aditya"

console.log("The String is : ",sample)

console.log("Using toUpperCase Method : " ,sample.toUpperCase());

console.log("The String is : ",sample)



/*

----------------------output-----------------------------

The String is :  Hii I am Penke Hemanth Aditya
Using toUpperCase Method :  HII I AM PENKE HEMANTH ADITYA
The String is :  Hii I am Penke Hemanth Aditya

*/