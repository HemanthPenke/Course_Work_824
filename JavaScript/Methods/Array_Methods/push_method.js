// -------------------Push Method-------------------------

// Push Method is used to add elements in the last of the array

arr = [24, 43, 54, 76, 45, 34, 87, 27]

console.log("Before Using Push Method  : ", arr)

arr.push(2400);

console.log("After Using Push Method  : ", arr)



/*

--------------------Output-------------------

Before Using Push Method  :  [ 24, 43, 54, 76, 45, 34, 87, 27 ]
After Using Push Method  :  [ 24, 43, 54, 76, 45, 34, 87, 27, 24 ]

*/