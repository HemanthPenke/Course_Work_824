// -------------------Shift Method-------------------------

// Shift Method is used to remove first elements of the array

arr = [24, 43, 54, 76, 45, 34, 87, 27]

console.log("Before Using Shift Method  : ", arr)

arr.shift();

console.log("After Using Shift Method  : ", arr)



/*

--------------------Output-------------------

Before Using Shift Method  :  [ 24, 43, 54, 76, 45, 34, 87, 27 ]
After Using Shift Method  :  [ 43, 54, 76, 45, 34, 87, 27 ]

*/