// -------------------unshift Method-------------------------

// Unshift Method is used to add elements in the first of the array

arr = [24, 43, 54, 76, 45, 34, 87, 27]

console.log("Before Using Unshift Method  : ", arr)

arr.unshift(824);

console.log("After Using Unshift Method  : ", arr)



/*

--------------------Output-------------------

Before Using Unshift Method  :  [ 24, 43, 54, 76, 45, 34, 87, 27 ]
After Using Unshift Method  :  [ 824, 24, 43, 54, 76, 45, 34, 87, 27 ]

*/