//---------------------toLowerCase method----------------------

//return the all characters into the lower case

//The toLowerCase() method does not change the original string.

var sample ="Hii I am Penke Hemanth Aditya"

console.log("The String is : ",sample)

console.log("Using toLowerCase Method : " ,sample.toLowerCase());

console.log("The String is : ",sample)



/*

----------------------output-----------------------------

The String is :  Hii I am Penke Hemanth Aditya
Using toLowerCase Method :  hii i am penke hemanth aditya
The String is :  Hii I am Penke Hemanth Aditya

*/