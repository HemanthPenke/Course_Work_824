//-------------------------join method----------------

//join method used to seperate array by using an element 


let sports=["criket","valley","hockey","football"]

console.log("Before using join method : " , sports);

console.log("After using join method : " , sports.join("----"));





/*

----------------Output--------------
Before using join method :  [ 'criket', 'valley', 'hockey', 'football' ]
After using join method :  criket----valley----hockey----football

*/
