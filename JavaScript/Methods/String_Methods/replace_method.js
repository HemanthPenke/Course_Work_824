//---------------------replace method----------------------

//The replace() method searches a string for a value or a regular expression.

//The replace() method returns a new string with the value(s) replaced.

//The replace() method does not change the original string.

var sample ="Hii I am Penke Hemanth Aditya"

console.log("The String is : ",sample)

console.log("Using replace Method : " ,sample.replace("Hemanth","Vikram"));

console.log("The String is : ",sample)



/*

----------------------output-----------------------------

The String is :  Hii I am Penke Hemanth Aditya
Using replace Method :  Hii I am Penke Vikram Aditya
The String is :  Hii I am Penke Hemanth Aditya

*/